// Select the menu icon and navbar elements
let menuIcon = document.querySelector('#menu-icon'); // Ensure this matches the ID in your HTML
let navbar = document.querySelector('.navbar');

// Add a click event listener to the menu icon
menuIcon.onclick = () => {
    menuIcon.classList.toggle('fa-xmark'); // Toggle the menu icon class
    navbar.classList.toggle('active'); // Toggle the navbar visibility
};

// Update the active class for navigation links on scroll
let sections = document.querySelectorAll('section');
let navlinks = document.querySelectorAll('header nav a');

window.onscroll = () => {
    sections.forEach(sec => {
        let top = window.scrollY;
        let offset = sec.offsetTop - 150;
        let height = sec.offsetHeight; 
        let id = sec.getAttribute('id');

        if (top >= offset && top < offset + height) {
            navlinks.forEach(link => {
                link.classList.remove('active');
                document.querySelector(`header nav a[href*=${id}]`).classList.add('active');
            });
        }
    });

    let header = document.querySelector('header');
    header.classList.toggle('sticky', window.scrollY > 100);

    menuIcon.classList.remove('fa-xmark');
    navbar.classList.remove('active');
};

